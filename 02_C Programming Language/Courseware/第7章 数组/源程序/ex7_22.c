#include "stdio.h"
int main(void)
{	int x[2][3],a=2;	    /*声明2*3数组x*/
	x[0][2]=8;		    /*将8赋给元素x[0][2]*/
	scanf("%d",&x[1][2]);       /*用&操作取元素x[1][2]的地址*/
	x[1][1]=x[0][2];	    /*将元素x[0][2]赋给元素x[1][1]*/
	x[1][2]<<=a;	            /*将元素x[1][2]的内容左移2位*/
	printf("%d\n",x[1][2]);     /*输出元素x[1][2]的值*/
	return 0;
}
