#include<stdio.h>
/****************************************************************
函数名称：mystrlen
函数功能：求字符串的长度
函数参数：s保存字符串的数组
函数返回值：返回字符串的长度。
****************************************************************/
int mystrlen(char s[])
{
	int i;
	i=0;
	while(s[i]!='\0') ++i;
	return i;
}
int main(void)
{
	printf("%d\n",mystrlen("world"));  /* 输出 5 */
	return 0;
}
