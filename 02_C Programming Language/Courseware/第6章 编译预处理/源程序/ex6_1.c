/******************************************************************
简要描述：由键盘输入y值，求表达式 3(y^2+3y)+4(y^2+3y)+y(y^2+3y) 的值
          y^2 表示 y的平分
*******************************************************************/
#include <stdio.h>
#define   M   (y*y+3*y)
int main(void)
{
	int  s,y;
	printf("Input a number:");
	scanf("%d",&y);
	s=3*M+4*M+y*M;
	printf("s=%d\n",s);
	return 0;
}
